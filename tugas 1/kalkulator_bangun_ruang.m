% --- Executes on button press in hitung.
function hitung_Callback(hObject, eventdata, handles)
% hObject    handle to hitung (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
p = str2double(get(handles.panjang,'string'));
l = str2double(get(handles.lebar,'string'));
t = str2double(get(handles.tinggi,'string'));
if get(handles.prisma,'value')==1
    sm = sqrt(p^2 + l^2);
    L = p*l + t*(p+l+sm);
    V = 0.5*p*l*t;
elseif get(handles.balok,'value')==1
    L = 2*p*l + 2*p*t + 2*l*t;
    V = p*l*t;
else
    sm1 = sqrt((0.5*p)^2 + t^2);
    sm2 = sqrt((0.5*l)^2 + t^2);
    L = p*l + l*sm1 + p*sm2;
    V = 1/3 * p*l*t;
end
set(handles.volume,'string',num2str(V));
set(handles.luas_permukaan,'string',num2str(L));

